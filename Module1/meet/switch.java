public class Switch
{
	public static void main(String args[])
	{
		int num=2;
		
		switch(num)
		{
			case 1:System.out.println("guj");
			break;
			
			case 2:System.out.println("hindi");
			break;
			
			case 3:System.out.println("eng");
			break;
			
			default:System.out.println("number is invalide");
			break;
		}
	}	
}